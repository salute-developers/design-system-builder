const PALETTE_REFS = new Set(["[general.green.400]","[general.orange.400]","[general.red.400]","[general.blue.400]","[general.green.800]","[general.blue.800]","[general.red.800]","[general.orange.800]","[general.green.600]","[general.orange.600]","[general.red.600]","[general.herbal.300]","[general.blue.600]","[general.blue.300]","[general.red.300]","[general.orange.300]","[general.green.300]","[general.gray.950]","[general.gray.900]","[general.gray.850]","[general.gray.50]","[general.green.500]","[general.orange.500]","[general.red.500]","[general.green.900]","[general.blue.500]","[general.orange.900]","[general.red.900]","[general.blue.900]","[general.gray.100]","[general.gray.150]","[general.gray.200]","[general.gray.1000]","[general.green.150]","[general.red.150]","[general.orange.150]","[general.blue.150]","[general.gray.800]","[general.gray.500]","[general.gray.700]","[general.gray.300]","[general.amber.300]","[general.amber.700]","[general.amber.300][0.56]","[general.amber.200]"]);

function isPaletteRef(val: string): boolean {
  return PALETTE_REFS.has(val);
}

const PALETTE_REF_RE = /^\[(\w+)\.(\w+)\.(\d+)\](?:\[([\d.]+)\])?$/;

function parsePaletteRef(ref: string): { paletteKey: string; opacity: string | null } {
  const m = ref.match(PALETTE_REF_RE)!;
  const paletteKey = `[${m[1]}.${m[2]}.${m[3]}]`;
  const opacity = m[4] ?? null;
  return { paletteKey, opacity };
}

async function resolvePaletteId(db: any, paletteKey: string): Promise<string> {
  const m = paletteKey.match(/^\[(\w+)\.(\w+)\.(\d+)\]$/)!;
  const [row] = await db
    .select({ id: schema.palette.id })
    .from(schema.palette)
    .where(
      and(
        eq(schema.palette.type, m[1] as 'general' | 'additional'),
        eq(schema.palette.shade, m[2]),
        eq(schema.palette.saturation, Number(m[3])),
      ),
    )
    .limit(1);
  if (!row) throw new Error(`Unknown palette reference: ${paletteKey}`);
  return row.id;
}

export async function seedColorTokenValues(
  db: any,
  tokenMap: Record<string, any>,
  tenantId: string,
) {
  // Resolve unique palette keys
  const paletteIdCache = new Map<string, string>();
  for (const val of Object.values(VALUES)) {
    if (isPaletteRef(val)) {
      const { paletteKey } = parsePaletteRef(val);
      if (!paletteIdCache.has(paletteKey)) {
        paletteIdCache.set(paletteKey, await resolvePaletteId(db, paletteKey));
      }
    }
  }

  const platforms = ['web', 'ios', 'android'] as const;
  const rows: any[] = [];

  for (const [name, val] of Object.entries(VALUES)) {
    // Derive mode and stripped token name from prefix
    const mode = name.startsWith('light.') ? 'light' as const : 'dark' as const;
    const strippedName = name.replace(/^(dark|light)\./, '');
    const token = tokenMap[strippedName];
    if (!token) continue;
    if (isPaletteRef(val)) {
      const { paletteKey, opacity } = parsePaletteRef(val);
      const paletteId = paletteIdCache.get(paletteKey)!;
      for (const platform of platforms) {
        rows.push({
          tokenId: token.id,
          tenantId,
          platform,
          mode,
          value: opacity !== null ? [opacity] : null,
          paletteId,
        });
      }
    } else {
      for (const platform of platforms) {
        rows.push({ tokenId: token.id, tenantId, platform, mode, value: [val], paletteId: null });
      }
    }
  }

  const BATCH_SIZE = 500;
  for (let i = 0; i < rows.length; i += BATCH_SIZE) {
    await db.insert(schema.tokenValues).values(rows.slice(i, i + BATCH_SIZE));
  }

  console.log(`    color: ${rows.length} rows`);
  return rows.length;
}